from math import gcd
import numbertheory
limit = 120000

rmem = {}


def rad(n):
    if n not in rmem:
        s = 1
        for p, e in numbertheory.factor(n):
            s *= p
        rmem[n] = s
    return rmem[n]


s = 0
abchits = []
for b in range(limit):
    for a in range(1, min(b, limit-b)):
        c = a + b
        if not gcd(a, b) == 1:
            continue
        if not gcd(a, c) == 1 or not gcd(b, c) == 1:
            continue
        if rad(a)*rad(b)*rad(c) < c:
            abchits += [(a, b, c)]
            s += c
print(s)

